# Ejercicio 9: Uso de replace() y upper()

# 1. Cadena inicial
texto = "Any time y Anytime"

# 2. Reemplazar "Any time" por "Always"
texto_reemplazado = texto.replace("Any time", "Always")

# 3. Convertir todo a mayúsculas
texto_final = texto_reemplazado.upper()

# Mostrar resultados 
print("Texto original:", texto)
print("Texto reemplazado:", texto_reemplazado)
print("Texto final:", texto_final)

